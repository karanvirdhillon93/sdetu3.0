package content;

public class Days {
	public static void main(String[] args) {
		//Execute different block of code based on the value of cond
		
		String dayOfWeek;
		dayOfWeek="";
		switch (dayOfWeek) {
		case "Monday": System.out.print("Monday");break;
		case "Tuesday": System.out.print("Tuesday");break;
		case "Wed":System.out.print("Wed"); break;
		case "Thu":System.out.print("Thu"); break;
		default:break;
		}
	}
}
